<h2>Estrutura de repetição</h2>
<hr>
<p>
    Utilizada para repetir um conjunto de instruções.
</p>
<strong>Exemplo</strong>
<pre>
    while(condição){
        //comandos a serem repeditos.
    }
</pre>

<?php
    $contador= 0;
    while($contador <=10){
        echo "Hello Word <br>";
        $contador++;
    }




?>